	public int MaxArea(String[] bs){
		int ans = 0;
		R = bs.length;
		C = bs[0].length();
		b = new boolean[R][C];
		dp = new int[R][C];
		for(int i = 0; i < R; i++){
			String s = bs[i];
			for(int j = 0; j < C; j++){
				char c = s.charAt(j);
				if(c == '1'){
					b[i][j] = true;
				}
			}
		}
		for(int i = 0; i < R; i++){
			for(int j = 0; j < C; j++){
				boolean st = b[i][j];
				boolean pr = !st;
				int cnt = 0;
				for(int k = 0; j+k < C; k++){
					if(b[i][j+k] == pr){
						break;
					}
					cnt++;
					pr = b[i][j+k];
				}
				dp[i][j] = cnt;
			}
		}
		
		for(int i = 0; i < R; i++){
			for(int j = 0; j < C; j++){
				boolean st = b[i][j];
				boolean pr = !st;
				for(int k = 0; j+k < C; k++){
					if(b[i][j+k] == pr){
						break;
					}
					ans = max(ans, get(i, j, k+1, st));
					pr = !pr;
				}
			}
		}
		
		
		return ans;
	}
	
	int get(int y, int x, int l, boolean st){
		if(y >= R || x >= C || x + l > C) return 0;

		if(b[y][x] != st) return 0;
		if(dp[y][x] >= l){
			return l + get(y+1, x, l, !st);
		}
		
		return 0;
	}
	
